def add_numbers(a, b):
    """
    Adds two numbers and returns the result.

    Parameters:
    a (float or int): The first number.
    b (float or int): The second number.

    Returns:
    float or int: The sum of the two numbers.
    """
    return a + b

# Example usage
result = add_numbers(5, 3)
print(f"The sum is: {result}")
